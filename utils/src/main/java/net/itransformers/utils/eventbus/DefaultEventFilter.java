import java.util.Map;

public class DefaultEventFilter implements EventFilter{
    private Map<String, String> eventAttributes;

    public DefaultEventFilter(Map<String, String> eventAttributes) {
        this.eventAttributes = eventAttributes;
    }
    /**
      * Returns true if event has to be filtered.
     */
    @Override
    public boolean filterEvent(Event event) {
        Map<String, Object> attributes = event.getAttributes();
        for (String key : eventAttributes.keySet()) {
            if (attributes.get(key) == null || !attributes.get(key).equals(eventAttributes.get(key))){
                return true;
            }
        }
        return false;
    }
}
